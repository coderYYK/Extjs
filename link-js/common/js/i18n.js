var isMyCIMLabelOrMsg = function (id) {
  return id !== undefined && id !== "" && id.substr(0, 2) === "I_";
};

var myCIMLabel = function (key) {
  return isMyCIMLabelOrMsg(key) ? i18n.labels[key.substr(2)] : key;
};

